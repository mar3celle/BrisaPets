package com.brisapets.webapp.config;

import com.brisapets.webapp.service.UserService;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.security.authentication.dao.DaoAuthenticationProvider;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.config.annotation.web.configuration.EnableWebSecurity;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.security.web.SecurityFilterChain;
import org.springframework.security.web.util.matcher.AntPathRequestMatcher;

@Configuration
@EnableWebSecurity
public class SecurityConfig {

    private final UserService userService;
    private final PasswordEncoder passwordEncoder;

    public SecurityConfig(UserService userService, PasswordEncoder passwordEncoder) {
        this.userService = userService;
        this.passwordEncoder = passwordEncoder;
    }

    // Regista o DaoAuthenticationProvider corretamente
    @Bean
    public DaoAuthenticationProvider authenticationProvider(PasswordEncoder encoder, UserDetailsService userDetailsService) {
        DaoAuthenticationProvider auth = new DaoAuthenticationProvider();
        auth.setUserDetailsService(userDetailsService);
        auth.setPasswordEncoder(encoder);
        return auth;
    }

    // Exp√µe o userService como UserDetailsService
    @Bean
    public UserDetailsService userDetailsService() {
        return userService;
    }

    @Bean
    public SecurityFilterChain securityFilterChain(HttpSecurity http) throws Exception {
        http
                .authorizeHttpRequests(requests -> requests
                        // 1. Recursos Est√°ticos (Sempre no topo)
                        .requestMatchers("/css/**", "/js/**", "/images/**", "/webjars/**").permitAll() // ‚úÖ Adicionado /webjars/** para ser seguro

                        // 2. Rotas de Autentica√ß√£o e Index
                        .requestMatchers("/", "/entrar", "/login", "/register", "/autenticar", "/perform_login").permitAll()

                        // Debug endpoint (temporary)
                        .requestMatchers("/debug/**").authenticated()

                        // ROTA DO PAINEL ADMIN: Requer a ROLE_ADMIN (mais idiom√°tico)
                        .requestMatchers("/admin").hasRole("ADMIN")

                        // O resto das rotas s√£o privadas (ex: /perfil, /pets, /appointments)
                        .anyRequest().authenticated()
                )
                .formLogin(form -> form
                        .loginPage("/autenticar")
                        .loginProcessingUrl("/perform_login")
                        .usernameParameter("username")
                        .passwordParameter("password")
                        .defaultSuccessUrl("/pets", true)
                )
                .logout(logout -> logout
                        .logoutUrl("/logout")
                        .logoutRequestMatcher(new AntPathRequestMatcher("/logout", "GET")) // Adiciona o RequestMatcher
                        .logoutSuccessUrl("/")
                        .invalidateHttpSession(true) // üåü NOVO: Invalida a sess√£o
                        .deleteCookies("JSESSIONID") // üåü NOVO: Deleta cookies de sess√£o
                        .permitAll()
                )
                // üåü NOVO: Tratamento de Exce√ß√£o para Acesso Negado (403 Forbidden)
                .exceptionHandling(exceptions -> exceptions
                        .accessDeniedPage("/403") // Redireciona para uma p√°gina de erro 403
                );


        return http.build();
    }
}
